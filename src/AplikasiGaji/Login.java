    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // try login
        try{
            Statement stat = (Statement)ConnectionDB.connectDatabase().createStatement();
            String sql     = "SELECT * FROM admin WHERE username='"+txtUsername.getText()+"' AND password='"+txtPassword.getText()+"'";
            ResultSet rs   = stat.executeQuery(sql);
            if (rs.next()){
                if(txtUsername.getText().equals(rs.getString("username")) && txtPassword.getText().equals(rs.getString("password"))){
                    JOptionPane.showMessageDialog(null, "Login Berhasil");
                    MainMenu tampil_mainmenu = new MainMenu();
                    tampil_mainmenu.setVisible(true);
                    this.dispose();
                }
            } else if (txtUsername.getText().equals("") && txtPassword.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Silahkan masukan Username dan Password terlebih dahulu");
                txtUsername.requestFocus();
            } else if(txtUsername.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Silahkan Masukan Username");
                txtUsername.requestFocus();
            } else if(txtPassword.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Silahkan Masukan Password");
                txtPassword.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Username atau Password salah!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

